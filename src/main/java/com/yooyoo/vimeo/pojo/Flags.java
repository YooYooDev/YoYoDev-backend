package com.yooyoo.vimeo.pojo;

public class Flags {
	

	 private String preload_video;
	 private float plays;
	 private float log;
	 private float dnt;
	 private float partials;
	 private float autohide_controls;


	 // Getter Methods 

	 public String getPreload_video() {
	  return preload_video;
	 }

	 public float getPlays() {
	  return plays;
	 }

	 public float getLog() {
	  return log;
	 }

	 public float getDnt() {
	  return dnt;
	 }

	 public float getPartials() {
	  return partials;
	 }

	 public float getAutohide_controls() {
	  return autohide_controls;
	 }

	 // Setter Methods 

	 public void setPreload_video(String preload_video) {
	  this.preload_video = preload_video;
	 }

	 public void setPlays(float plays) {
	  this.plays = plays;
	 }

	 public void setLog(float log) {
	  this.log = log;
	 }

	 public void setDnt(float dnt) {
	  this.dnt = dnt;
	 }

	 public void setPartials(float partials) {
	  this.partials = partials;
	 }

	 public void setAutohide_controls(float autohide_controls) {
	  this.autohide_controls = autohide_controls;
	 }


}
