package com.yooyoo.vimeo.pojo;

public class Thumbs {
	 private String abc = new String("640");
	 private String def = new String("960");
	 private String ghi = new String("1280");
	 private String base;


	 // Getter Methods 

	 public String getAbc() {
	  return abc;
	 }

	 public String getDef() {
	  return def;
	 }

	 public String getGhi() {
	  return ghi;
	 }

	 public String getBase() {
	  return base;
	 }

	 // Setter Methods 

	 public void setAbc(String abc) {
	  this .abc = abc;
	 }

	 public void setDef(String def) {
	  this .def = def;
	 }

	 public void setGhi(String ghi) {
	  this .ghi = ghi;
	 }

	 public void setBase(String base) {
	  this.base = base;
	 }
}
